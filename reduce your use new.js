		    function ropeInitIfNeeded(a, b) {
		      if (ropePts.length === ROPE_POINTS) return;
		      ropePts.length = 0;
		      for (var i = 0; i < ROPE_POINTS; i++) {
		        var t = (ROPE_POINTS === 1) ? 0 : (i / (ROPE_POINTS - 1));
		        var x = a.x + (b.x - a.x) * t;
		        var y = a.y + (b.y - a.y) * t;
		        ropePts.push({ x: x, y: y, px: x, py: y });
		      }
		      ropeRestLen = distXY(a.x, a.y, b.x, b.y) * ROPE_SLACK;
		      ropeRestLen = Math.max(60, ropeRestLen);
		    }
		
		    function ropeReset(a, b) { ropePts.length = 0; ropeInitIfNeeded(a, b); }
		
		    function ropeSim(a, b) {
		      ropeInitIfNeeded(a, b);
		
		      var targetLen = distXY(a.x, a.y, b.x, b.y) * ROPE_SLACK;
		      targetLen = Math.max(60, targetLen);
		      ropeRestLen += (targetLen - ropeRestLen) * 0.12;
		
		      for (var i = 1; i < ropePts.length - 1; i++) {
		        var p = ropePts[i];
		        var vx = (p.x - p.px) * ROPE_DAMPING;
		        var vy = (p.y - p.py) * ROPE_DAMPING;
		        p.px = p.x; p.py = p.y;
		        p.x += vx;
		        p.y += vy + ROPE_GRAVITY;
		      }
		
		      ropePts[0].x = a.x; ropePts[0].y = a.y; ropePts[0].px = a.x; ropePts[0].py = a.y;
		      var last = ropePts.length - 1;
		      ropePts[last].x = b.x; ropePts[last].y = b.y; ropePts[last].px = b.x; ropePts[last].py = b.y;
		
		      var segLen = ropeRestLen / (ropePts.length - 1);
		
		      for (var it = 0; it < ROPE_ITER; it++) {
		        for (i = 0; i < ropePts.length - 1; i++) {
		          var p1 = ropePts[i];
		          var p2 = ropePts[i + 1];
		          var dx = p2.x - p1.x;
		          var dy = p2.y - p1.y;
		          var d = Math.sqrt(dx*dx + dy*dy);
		          if (d < 0.001) d = 0.001;
		
		          var diff = (d - segLen) / d;
		          var offx = dx * diff * 0.5;
		          var offy = dy * diff * 0.5;
		
		          if (i !== 0) { p1.x += offx; p1.y += offy; }
		          if (i + 1 !== last) { p2.x -= offx; p2.y -= offy; }
		        }
		        ropePts[0].x = a.x; ropePts[0].y = a.y;
		        ropePts[last].x = b.x; ropePts[last].y = b.y;
		      }
		    }
		
		    function ropeDrawSmooth() {
		      rope.graphics.clear();
		      if (!selectedAppliance || !plug.visible || plug.alpha <= 0.01) return;
		
		      var parent = rope.parent || stage;
		      if (!parent.globalToLocal) return;
		
		      var g = rope.graphics;
		      g.setStrokeStyle(ROPE_THICKNESS, "round", "round").beginStroke(ROPE_COLOR);
		
		      var p0 = parent.globalToLocal(ropePts[0].x, ropePts[0].y);
		      g.moveTo(p0.x, p0.y);
		
		      for (var i = 1; i < ropePts.length - 2; i++) {
		        var p1 = parent.globalToLocal(ropePts[i].x, ropePts[i].y);
		        var p2 = parent.globalToLocal(ropePts[i + 1].x, ropePts[i + 1].y);
		        var mx = (p1.x + p2.x) * 0.5;
		        var my = (p1.y + p2.y) * 0.5;
		        g.quadraticCurveTo(p1.x, p1.y, mx, my);
		      }
		
		      var pLastCtrl = parent.globalToLocal(ropePts[ropePts.length - 2].x, ropePts[ropePts.length - 2].y);
		      var pLast = parent.globalToLocal(ropePts[ropePts.length - 1].x, ropePts[ropePts.length - 1].y);
		      g.quadraticCurveTo(pLastCtrl.x, pLastCtrl.y, pLast.x, pLast.y);
		
		      g.endStroke();
		    }
		
