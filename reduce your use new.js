		    // sandwich depth:
		    // 1) cable directly under selected
		    // 2) any display objects that ended up between cable and selected are pushed below cable
		    function enforceCableSandwich() {
		      if (!selectedAppliance) return;
		
		      var parent = ensureCableInSameParentAsSelected();
		      if (!parent || !parent.getChildIndex || !parent.setChildIndex) return;
		
		      // If plug was on a different parent, also move it to the same parent (optional but stabilizes)
		      try { reparentKeepGlobal(plug, parent); } catch(e){}
		
		      var apChild = selectedAppliance; // selected is already direct child under that parent in your file
		      var cableChild = cableLayer;
		
		      // Step 1: adjacency behind selected
		      var apIdx = parent.getChildIndex(apChild);
		      var desired = Math.max(0, apIdx - 1);
		      parent.setChildIndex(cableChild, desired);
		
		      // Step 2: push anything between cable and selected below cable
		      var cableIdx = parent.getChildIndex(cableChild);
		      var selectedIdx = parent.getChildIndex(apChild);
		
		      if (cableIdx < selectedIdx - 1) {
		        for (var i = cableIdx + 1; i <= selectedIdx - 1; i++) {
		          var ch = parent.getChildAt(i);
		          if (ch === apChild || ch === cableChild) continue;
		          parent.setChildIndex(ch, cableIdx);
		          cableIdx = parent.getChildIndex(cableChild);
		          selectedIdx = parent.getChildIndex(apChild);
		        }
		      }
		
		      // re-assert adjacency
		      apIdx = parent.getChildIndex(apChild);
		      desired = Math.max(0, apIdx - 1);
		      parent.setChildIndex(cableChild, desired);
		    }
		
